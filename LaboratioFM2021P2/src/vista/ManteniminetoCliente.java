    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Cliente");
        modelo.addColumn("Nombre Cliente");
        modelo.addColumn("Apellido Cliente");
        modelo.addColumn("Edad Cliente");
        modelo.addColumn("Correo Cliente");
        modelo.addColumn("Direccion Cliente");
        modelo.addColumn("Telefono Cliente");
        modelo.addColumn("DPI Cliente");
       

        ClientesDAO clienteDAO = new ClientesDAO();

        List<Clientes> clientesDAO = clienteDAO.select();
        TablaCliente.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < clientesDAO.size(); i++) {
            dato[0] = Integer.toString(clientesDAO.get(i).getPK_id_cliente());
            dato[1] = clientesDAO.get(i).getNombre_cliente();
            dato[2] = clientesDAO.get(i).getApellido_cliente();
            dato[3] = clientesDAO.get(i).getEdad_cliente();
            dato[4] = clientesDAO.get(i).getCodigoCorreo_cliente();
            dato[5] = clientesDAO.get(i).getCodigoDireccion_cliente();
            dato[6] = clientesDAO.get(i).getCodigoTelefono_cliente();
            dato[7] = clientesDAO.get(i).getDPI_cliente();
          

            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

